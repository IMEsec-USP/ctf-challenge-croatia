<h1>Sobre a croácia</h1>
<p>Croácia (em croata: Hrvatska), oficialmente República da Croácia, 
é um país europeu que se limita ao norte com a Eslovénia e Hungria, 
a nordeste com a Sérvia, a leste com a Bósnia e Herzegovina e ao sul com Montenegro. 
É banhado a oeste pelo mar Adriático e possui uma fronteira marítima com a Itália, no golfo de Trieste.
</p>
